# Состояния: название берём из игры ($data_states), описание — из таблицы StateHelp.
# Перенос строк считаем здесь по уже собранной строке (имя из игры имеет другую
# ширину, чем старое хардкод-имя), поэтому таблицу заранее НЕ оборачиваем.
class Window_State < Window_Selectable
	def description
		return "" unless @data[index]
		if @data[index].is_a?(Array)
			return StateHelp::Buffs[@data[index]]
		else
			id = @data[index].id
			state = $data_states[id]
			name = state ? state.name : ""
			# приоритет — сжатый Overrides, иначе таблица; без ведущего 【Имя】 чтобы не дублировать
			src = (StateHelp::Overrides[id] || StateHelp::States[id]).to_s
			desc = src.sub(/\A【[^】]*】/, "")
			wndfix_wrap_text("【#{name}】#{desc}")
		end
	end

	# Перенос строк по ширине окна помощи (учитывает имя из игры + запас под хвост)
	def wndfix_wrap_text(text)
		# мерим РЕАЛЬНЫМ шрифтом окна помощи, а не дефолтным Bitmap-шрифтом
		hc = (@help_window && @help_window.contents) ? @help_window.contents : nil
		bmp = hc || Bitmap.new(1, 1)
		# text_size в RGSS немного занижает фактическую ширину — оставляем запас в символ
		max_width = (hc ? @help_window.contents_width : Graphics.width - 12 * 2)
		max_width -= bmp.text_size("。").width
		space_w = bmp.text_size(" ").width
		result = text.to_s.split("\n", -1).map do |line|
			out = ""
			line_w = 0
			line.split(" ").each do |word|
				w = bmp.text_size(word).width
				if line_w > 0 && line_w + space_w + w > max_width
					out += "\n"
					line_w = 0
				elsif line_w > 0
					out += " "
					line_w += space_w
				end
				out += word
				line_w += w
			end
			out
		end.join("\n")
		bmp.dispose unless hc
		result
	end
end

# Фикс описание в окне выдачи
class Window_Getinfo < Window_Base
  alias wndfix_orig_description description
  def description(data)
    desc = wndfix_orig_description(data)
    # Убрать все команды на строчке
    desc.sub!(/\\.(?:\[\w+\])/i, "")
    desc
  end
end

