#--------------------------------------------------------------------------
# Исправленная логика обработки ввода имени
#--------------------------------------------------------------------------
class Scene_Name < Scene_MenuBase
  
  alias lok_on_input_ok_v15 on_input_ok
  def on_input_ok
    name = @edit_window.name.to_s.force_encoding("UTF-8")
    check = ->(hash) { hash.find { |k, _| Regexp.new("^#{Regexp.escape(k)}$", Regexp::IGNORECASE) === name } }
    
    # 1. Проверяем СМЕРТЕЛЬНЫЕ имена
    if (res = check.call(LOK_NAME_REACTION::DEATH_NAMES))
      show_system_message(res[1])
      
      # Регистрируем вызов Game Over строго ПОСЛЕ окончания диалога
      $game_message.add_case_for_death = true 
      
      # Переходим на карту, чтобы диалог корректно отрисовался
      SceneManager.goto(Scene_Map) 
      
    # 2. Проверяем ОСОБЫЕ имена (Смена персонажа)
    elsif (res = check.call(LOK_NAME_REACTION::SPECIAL_NAMES))
      $game_temp.special_char_id = res[1][0] 
      show_system_message(res[1][1])
      lok_on_input_ok_v15
      
    # 3. Проверяем ОБЫЧНЫЕ имена со стёбом
    elsif (res = check.call(LOK_NAME_REACTION::REACTIONS))
      show_system_message(res[1])
      lok_on_input_ok_v15
    else
      lok_on_input_ok_v15
    end
  end
  
  def show_system_message(text)
    $game_system.use_name_window = true
    $game_temp.direct_show_name = LOK_NAME_REACTION::SENDER_NAME
    $game_message.face_name = LOK_NAME_REACTION::SYSTEM_FACE[0]
    $game_message.face_index = LOK_NAME_REACTION::SYSTEM_FACE[1]
    $game_message.add(text)
  end
end

#--------------------------------------------------------------------------
# ХУК НА КАРТУ: Убиваем игру только тогда, когда текст закрылся
#--------------------------------------------------------------------------
class Scene_Map < Scene_Base
  alias lok_gameover_update update
  def update
    lok_gameover_update
    # Если был запущен смертельный диалог и он закончился (окно пропало)
    if $game_message.add_case_for_death && !$game_message.visible
      $game_message.add_case_for_death = false
      SceneManager.goto(Scene_Gameover) # Вот теперь включаем Game Over!
    end
  end
end

